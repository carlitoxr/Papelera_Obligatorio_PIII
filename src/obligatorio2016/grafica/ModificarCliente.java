    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        rutCi = Long.parseLong(String.valueOf(jList1.getSelectedValue()));
        Cliente cte = Fachada.getInstancia().findCte(rutCi);

        jTextField1.setText(cte.getNomCte());
        jTextField2.setText(cte.getApeCte());
        jFormattedTextField1.setText(cte.getFechaIng());
    }//GEN-LAST:event_jList1MouseClicked

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        DefaultListModel modelo = new DefaultListModel();
        Enumeration<Cliente> eCte = Fachada.getInstancia().getDiccioCtes().getColeccion().elements();
        while(eCte.hasMoreElements()){
            Cliente cte = eCte.nextElement();
            modelo.addElement(cte.getRUT_CI());
            jList1.setModel(modelo);
        }
    }//GEN-LAST:event_formWindowOpened

