    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        Enumeration<Proveedor> eProv = Fachada.getInstancia().getDiccioProv().getColeccion().elements();
        while(eProv.hasMoreElements()){
            Proveedor prov = eProv.nextElement();
            jComboBox1.addItem(prov.getNomProv());
        }
    }//GEN-LAST:event_formWindowOpened

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nomProv = String.valueOf(jComboBox1.getSelectedItem());
        Producto prod = new Producto(Long.parseLong(jTextField1.getText()), jTextField2.getText(), Float.valueOf(jTextField3.getText()));
        
        Enumeration<Proveedor> eProv = Fachada.getInstancia().getDiccioProv().getColeccion().elements();
        while(eProv.hasMoreElements()){
            Proveedor prov = eProv.nextElement();
            if(prov.getNomProv().equals(nomProv)){
                Productos prods = prov.getProds();
                prods.insert(prod);
                prov.setProdXProv(prods);
            }
        }
        
        MsjExito exito = new MsjExito(3);
        JOptionPane.showMessageDialog(null, exito.getMsj(), "", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

